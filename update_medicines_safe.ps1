$updates = @{}
$currentId = $null
$currentBenefit = @()
$currentDose = @()

$lines = $data -split "`n" | ForEach-Object { $_.Trim() }

for ($i = 0; $i -lt $lines.Count; $i++) {
    $line = $lines[$i]
    if ([string]::IsNullOrWhiteSpace($line)) { continue }

    if ($line -match "^(\d+)-\s*(.*)") {
        if ($currentId -ne $null) {
            $benefitStr = $currentBenefit -join " "
            $doseStr = $currentDose -join " "
            if ($currentId -eq 249) {
                $doseStr = "Internal dose: 10 drops in warm milk; External use: Body application (Abhyanga)"
            }
            $updates[$currentId] = @{
                benefits = $benefitStr
                dose = $doseStr
            }
        }
        $currentId = [int]$matches[1]
        $firstLineBenefit = $matches[2]
        if ($firstLineBenefit.StartsWith("indication- ")) {
            $firstLineBenefit = $firstLineBenefit.Substring("indication- ".Length)
        }
        $currentBenefit = @($firstLineBenefit)
        $currentDose = @()
        
        $i++
        while ($i -lt $lines.Count -and -not ($lines[$i] -match "^\d+-")) {
            $doseLine = $lines[$i]
            if (-not [string]::IsNullOrWhiteSpace($doseLine)) {
                if ($doseLine.StartsWith("dose- ")) {
                    $doseLine = $doseLine.Substring("dose- ".Length)
                }
                $currentDose += $doseLine
            }
            $i++
        }
        $i-- 
    }
}

if ($currentId -ne $null) {
    $benefitStr = $currentBenefit -join " "
    $doseStr = $currentDose -join " "
    if ($currentId -eq 249) {
        $doseStr = "Internal dose: 10 drops in warm milk; External use: Body application (Abhyanga)"
    }
    $updates[$currentId] = @{
        benefits = $benefitStr
        dose = $doseStr
    }
}

Write-Host "Parsed $($updates.Count) updates."

function Update-JsonFile($filepath) {
    if (-not (Test-Path $filepath)) {
        return
    }
    $jsonObj = Get-Content -Path $filepath -Raw -Encoding UTF8 | ConvertFrom-Json
    $modifiedCount = 0
    foreach ($med in $jsonObj) {
        $id = $med.id
        if ($updates.ContainsKey($id)) {
            $med.benefits = $updates[$id].benefits
            $med.dose = $updates[$id].dose
            $modifiedCount++
        }
    }
    
    $jsonString = $jsonObj | ConvertTo-Json -Depth 100
    
    # Unescape \uXXXX
    $jsonString = [System.Text.RegularExpressions.Regex]::Replace($jsonString, "\\u(?<Value>[a-fA-F0-9]{4})", {
        param($m)
        [char][int]::Parse($m.Groups['Value'].Value, [System.Globalization.NumberStyles]::HexNumber)
    })
    
    $utf8NoBom = New-Object System.Text.UTF8Encoding($false)
    $tempFile = $filepath + ".tmp"
    [System.IO.File]::WriteAllText($tempFile, $jsonString, $utf8NoBom)
    
    Copy-Item $tempFile $filepath -Force
    Remove-Item $tempFile
    
    Write-Host "Updated $modifiedCount records in $filepath"
}

